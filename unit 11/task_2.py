class Math:

    def addition(self, x, y):
        return x + y

    def subtraction(self, x, y):
        return x - y

    def multiplication(self, x, y):
        return x * y

    def division(self, x, y):
        return x / y



m = Math()
print(m.addition(10,5))
print(m.subtraction(10,5))
print(m.multiplication(10,5))
print(m.division(10,5))
